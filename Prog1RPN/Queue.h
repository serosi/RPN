#ifndef __QUEUE_H
#define __QUEUE_H

typedef float QueueType;

class Queue {
private:
   static const int MAX_SIZE = 69;
   float queue[MAX_SIZE];
   int front, back;
public:
   //---------------------------------------------------------------- 
   // Default constructor for queue
   //---------------------------------------------------------------- 
   Queue();

   //---------------------------------------------------------------- 
   // Checks if queue is empty
   //---------------------------------------------------------------- 
   bool isEmpty();

   //---------------------------------------------------------------- 
   // Checks if queue is full
   //---------------------------------------------------------------- 
   bool isFull();

   //---------------------------------------------------------------- 
   // Adds an item to the queue
   //---------------------------------------------------------------- 
   void Enqueue(float value);

   //---------------------------------------------------------------- 
   // Removes an item from the queue
   //---------------------------------------------------------------- 
   void Dequeue(float& value);

   //---------------------------------------------------------------- 
   // Empties the stack by setting front & back values to 0
   //---------------------------------------------------------------- 
   void MakeEmpty();

   //---------------------------------------------------------------- 
   // Prints the entire queue
   //---------------------------------------------------------------- 
   void PrintQueue();

   //---------------------------------------------------------------- 
   // Returns last element of the queue
   //---------------------------------------------------------------- 
   float LastElement();

};
#endif